/**
 * Get popular tools
 */
export const getPopularTools = (): Tool[] => {
  return FOOTER_TOOLS.filter(tool => tool.isPopular);
};

/**
 * Get tool by ID
 */
export const getToolById = (id: string): Tool | undefined => {
  return FOOTER_TOOLS.find(tool => tool.id === id);
};

/**
 * Get tools by category
 */
export const getToolsByCategory = (category: ToolCategory): Tool[] => {
  return FOOTER_TOOLS.filter(tool => tool.category === category);
};
